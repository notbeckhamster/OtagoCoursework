package src;
/**
 * Class to store an array of integers and an integer value
 * 
 * @group BobbyTables
 * @author Beckham Wilson
 */
public class IntArrIntPair {
    int[] array;
    int value;

    /**
     * Constructor for IntArrIntPair
     * 
     * 
     * @param array
     * @param value
     */
    public IntArrIntPair(int[] array, int value) {
        this.array = array;
        this.value = value;
    }
}